                    Among the members of the church were two families who had been at odds for years. The Martins, led by the headstrong patriarch, Robert, and the Thompsons, whose matriarch, Evelyn, was equally resolute, had once been close friends. However, a business deal gone wrong had driven a wedge between them. Accusations flew, lawsuits ensued, and bitterness festered. Even their children, who had grown up as inseparable friends, were now strangers to one another..</p>
