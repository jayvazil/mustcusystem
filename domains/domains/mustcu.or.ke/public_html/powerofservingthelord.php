            <p>The moment Jacob walked into the kitchen, he was met with warmth and hospitality from the other volunteers. They didn’t treat him as a newcomer but as someone they were glad to have on the team. He felt a sense of belonging almost immediately, even though he was uncertain of his role.</p>
